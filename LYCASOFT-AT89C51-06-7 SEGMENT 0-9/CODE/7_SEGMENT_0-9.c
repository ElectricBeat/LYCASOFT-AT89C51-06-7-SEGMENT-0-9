#include<reg51.h>
#include "8051_delay.h"

char DIG[10] = {0x3f,0x06,0x5b,0x4f,0x66,0x6d,0x7d,0x07,0xff,0x67};			//comman cathode type;
//char DIG[10]={0xC0,0xF9,0xA4,0xB0,0x99,0x92,0x82,0xF8,0x80,0x90};			//comman anode type;

int Count;

void main()
{
	P2 = 0x00;
	
	while(1)
	{
		P2 = DIG[Count];
		delay(1000);
		Count++;
		if(Count >= 10)
		{
			Count = 0;
		}
	}
}